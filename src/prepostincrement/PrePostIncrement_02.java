package prepostincrement;

public class PrePostIncrement_02 {

	public static void main(String[] args) {
		int i = 0;
		int j = 0;
		
		System.out.println("first increment the value : " +i);
		System.out.println("first print the value : " +j);
		System.out.println("================================");
		System.out.println("first increment the value : " + ++i);
		System.out.println("first print the value : " + ++j);
		System.out.println("================================");
		System.out.println("first increment the value : " + i++);
		System.out.println("first print the value : " + j++);
		System.out.println("================================");
		System.out.println("first increment the value : " +i);
		System.out.println("first print the value : " +j);
	}

}
