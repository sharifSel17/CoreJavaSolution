package prepostincrement;

public class PrePostIncrement_01 {

	public static void main(String[] args) {
		int i = 0;
		int j = 0;
		
		int m = 0;
		int n = 0;
		
		System.out.println("first increment the value : " + ++i);
		System.out.println("first print the value : " + ++j);
		System.out.println("================================");
		System.out.println("first print the value whatever as it : "+ m--);
		System.out.println("first print the value whatever as it : "+ n--);
		System.out.println("================================");
		System.out.println("first print the value whatever as it : "+ m--);
		System.out.println("first print the value whatever as it : "+ n--);

	}
}
