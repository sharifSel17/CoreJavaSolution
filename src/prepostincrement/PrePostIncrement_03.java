package prepostincrement;

public class PrePostIncrement_03 {

	public static void main(String[] args) {
		int i=0;
		int j=0;
		int k=++i;
		int m=j++;

		System.out.println(k);
		System.out.println(m);
		System.out.println("======================");
		System.out.println(j);
		System.out.println(i);
		
	}

}
