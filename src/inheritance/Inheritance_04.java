package inheritance;

public class Inheritance_04 {
	public int a = 10;
	private int b = 20;
	protected int c = 30;
	int d = 40;

	public void test01() {
		System.out.println("parent test01");
	}

	private void test02() {
		System.out.println("parent test02");
	}

	protected void test03() {
		System.out.println("parent test03");
	}

	void test04() {
		System.out.println("parent test04");
	}

}
