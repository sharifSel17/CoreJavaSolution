package stringinjava;

public class StringInJava_02 {

	public static void main(String[] args) {
		// concat () method appends the string at the end
		String s1 = "sharif";
		s1.concat("uddin");
		System.out.println(s1);
		
		String s2 = "sharif";
		s2 = s2.concat("uddin");
		System.out.println(s2);
		
	}

}
